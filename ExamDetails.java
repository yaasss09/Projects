    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String s_name       =   jTextField1.getText();
        String date         =   jTextField2.getText();
        String address      =   jTextArea2.getText();
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/projectty?zeroDateTimeBehavior=convertToNull","root","09012002yaSH.");
            Statement GetStudent = con.createStatement();
            Statement exam = con.createStatement();
            String sql1 = "SELECT * FROM student_details where S_NAME = '"+s_name+"';";
            ResultSet b= GetStudent.executeQuery(sql1);
            b.next();
            int id = b.getInt("S_ID");
            String sql2 = "INSERT INTO exam_details(E_ID,EXAM_DATE,EXAM_ADDRESS)VALUES('"+id+"','"+date+"','"+address+"');";
            int x = exam.executeUpdate(sql2);
            if(x==1)
            {
                JOptionPane.showMessageDialog(this, "<html><font face='Candara Light' size='5'>Exam Schedule has been updated");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "<html><font face='Candara Light' color='red' size='5'>There was a problem updating Exam_Schedule","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton9ActionPerformed

